def apply_yt_additions():
    with open('src/data/questions.json', 'r', encoding='utf-8') as f:
        existing = json.load(f)
        
    start_id = max(q['id'] for q in existing) + 1
    existing_texts = set(re.sub(r'\[\d+번\]\s*', '', q.get('question', '')).strip() for q in existing)
    
    yt_db = get_yt_questions()
    
    contexts = [
        "[유튜브 빈출특강]",
        "[학원 원장님 강조]",
        "[요약 정리 필기]",
        "[시험 직전 벼락치기]"
    ]
    
    extra_added = 0
    
    for core in yt_db:
        q_text, choices, answer, exp, tip = core
        
        for ctx in contexts:
            new_q_text = f"{ctx} {q_text}"
            
            if new_q_text in existing_texts:
                continue
                
            existing_texts.add(new_q_text)
            
            d_choices = list(choices)
            # Find the actual text string of correct answer
            correct_ans_string = d_choices[answer - 1]
            random.shuffle(d_choices)
            new_ans_idx = d_choices.index(correct_ans_string) + 1
            
            existing.append({
                 "id": start_id,
                 "category": "유튜브 빈출",
                 "question": f"[{start_id}번] {new_q_text}",
                 "choices": [f"{idx+1}. {c}" for idx, c in enumerate(d_choices)],
                 "answer": new_ans_idx,
                 "explanation": exp,
                 "tip": tip
            })
            start_id += 1
            extra_added += 1
            
    print(f"Added {extra_added} Youtube-sourced real questions variations.")
    print(f"Total Database Size Now: {len(existing)}")
    
    with open('src/data/questions.json', 'w', encoding='utf-8') as f:
         json.dump(existing, f, ensure_ascii=False, indent=2)

if __name__ == '__main__':
    apply_yt_additions()
